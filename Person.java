public class Person {
    String name;
    String work;
    int age;
    boolean sex;
    int wage;

    public Person(String name, String work, int age, boolean sex, int wage){
        this.age = age;
        this.name = name;
        this.work = work;
        this.sex = sex;
        this.wage = wage;
    }
}